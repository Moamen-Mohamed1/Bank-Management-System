private void adjustComponentPositions() {
    mainMenu.setLayout(null); // Ensuring the layout is absolute for manual adjustments

    // Calculate new height based on the visibility of jPanel1
    int heightAdjustment = subPanel.isVisible() ? subPanel.getPreferredSize().height + 20 : 0;

    // Adjust jPanel1 dimensions
    Rectangle jPanelBounds = subPanel.getBounds();
    jPanelBounds.height = heightAdjustment;
    subPanel.setBounds(jPanelBounds);

    // Adjust jLabel7 position
    Rectangle jLabel7Bounds = DeleteAccount.getBounds();
    jLabel7Bounds.y = subPanel.getY() + heightAdjustment;  // Moving jLabel7 down when jPanel1 is expanded
    DeleteAccount.setBounds(jLabel7Bounds);

    // Force jPanel2 to re-layout and repaint
    mainMenu.revalidate();
    mainMenu.repaint();
}
