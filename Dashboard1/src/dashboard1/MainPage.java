   private void customizeTableCells() {
    jTable2.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            
            
            // Default background color settings for alternating rows
            if (row % 2 == 0) {
                setBackground(new Color(240, 240, 240)); // Lighter shade for alternate rows
            } else {
                setBackground(Color.WHITE); // White background for other rows
            }
            
            // Default text color and font settings
            setForeground(Color.BLACK); // Default text color
            setFont(new Font("Segoe UI", Font.BOLD, 15)); // Font settings
            if (column == 1) { // Assuming the Transaction Type is at column index 1
                if ("Withdraw".equals(value)) {
                    setText(value.toString()); // Add minus sign for withdrawals
                    setForeground(Color.RED); // Set text color to red for withdrawals
                } else if ("Deposit".equals(value)) {
                    setText(value.toString()); // Add plus sign for deposits
                    setForeground(new Color(0, 128, 0)); // Set text color to green for deposits
                }
            }
            // Check the transaction type and format the "Amount" column
            if (column == 2) {  // Assuming the "Amount" column is at column index 2
                Object typeValue = table.getModel().getValueAt(row, 1);  // Get the transaction type from column 1
                if ("Withdraw".equals(typeValue)) {
                    setText("- " + value.toString()); // Add minus sign for withdrawals
                    setForeground(Color.RED); // Set text color to red for withdrawals
                } else if ("Deposit".equals(typeValue)) {
                    setText("+ " + value.toString()); // Add plus sign for deposits
                    setForeground(new Color(0, 128, 0)); // Set text color to green for deposits
                }
            }

            // Align text to center in certain columns if necessary
            if (column == 0 || column == 2 || column == 3) {
                setHorizontalAlignment(JLabel.CENTER); // Center alignment for ID and Time columns
            } else {
                setHorizontalAlignment(JLabel.LEFT); // Left alignment for the Transaction Type column
            }
            return this;
            
        }
    });
}
private void addHoverEffect(javax.swing.JLabel label) {
    label.setOpaque(false);
    label.setForeground(Color.WHITE); // Initial text color

    label.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseEntered(java.awt.event.MouseEvent evt) {
            // On hover, make the label opaque and change the background color
            label.setOpaque(true);
            label.setBackground(Color.BLUE); // Solid color on hover
            label.setForeground(Color.YELLOW); // Change text color to contrast with the hover background
            label.setBorder(javax.swing.BorderFactory.createLineBorder(Color.YELLOW)); // Optional: add a border
        }

        @Override
        public void mouseExited(java.awt.event.MouseEvent evt) {
            // On mouse exit, revert the label to non-opaque to show the gradient again
            label.setOpaque(false);
            label.setBackground(null); // Clear any specific background color
            label.setForeground(Color.WHITE); // Revert text color
            label.setBorder(null); // Remove border if added
            label.repaint(); // Ensure the label repaints to reflect these changes
        }
    });
}
